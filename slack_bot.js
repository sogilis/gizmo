if (!process.env.token) {
    console.log('Error: Specify token in environment');
    process.exit(1);
}

var Botkit = require('./lib/Botkit.js');
var os = require('os');
var state = 'nobody';
var express = require('express');
var app     = express();

app.listen(process.env.PORT || 5000);

var controller = Botkit.slackbot({
    debug: true,
});

var bot = controller.spawn({
    token: process.env.token
}).startRTM();

controller.hears(['release'], 'direct_mention', function(bot, message) {
    controller.storage.users.get(message.user, function(err, user) {
      state = 'nobody'
      bot.reply(message, 'Gizmo released !');
    });
});

controller.hears(['take (.*)'], 'direct_mention', function(bot, message) {
  var name = message.match[1];
  controller.storage.users.get(message.user, function(err, user) {
    if (state == 'nobody') {
      state = name
      bot.reply(message, state + ' has Gizmo !');
    } else {
      bot.reply(message, 'You can\'t do that ! ' + state + ' has it !');
    }
  });
});

controller.hears(['state'], 'direct_mention', function(bot, message) {
  controller.storage.users.get(message.user, function(err, user) {
    bot.reply(message, 'Gizmo is owned by ' + state + ' !');
  });
});



controller.hears(['uptime', 'identify yourself', 'who are you', 'what is your name'],
    'direct_message,direct_mention,mention', function(bot, message) {

        var hostname = os.hostname();
        var uptime = formatUptime(process.uptime());

        bot.reply(message,
            ':robot_face: I am a bot named <@' + bot.identity.name +
             '>. I have been running for ' + uptime + ' on ' + hostname + '.');

    });

function formatUptime(uptime) {
    var unit = 'second';
    if (uptime > 60) {
        uptime = uptime / 60;
        unit = 'minute';
    }
    if (uptime > 60) {
        uptime = uptime / 60;
        unit = 'hour';
    }
    if (uptime != 1) {
        unit = unit + 's';
    }

    uptime = uptime + ' ' + unit;
    return uptime;
}
